package SamsungLG;

public class LGTV {

	public void switchOn() {
		System.out.println("LGTV : 전원 on");
	}
	public void switchOff() {
		System.out.println("LGTV : 전원 off");
	}
	public void soundUp() {
		System.out.println("LGTV : 소리 키움");
	}
	public void soundDown() {
		System.out.println("LGTV : 소리 낮춤");
	}
}
